package Curso;

/**
 *
 * @author angel
 */
public class AsignacionMasiva {
    
    private String curso;
    private String carne_estudiante;

    public AsignacionMasiva(String curso, String carne_estudiante) {
        this.curso = curso;
        this.carne_estudiante = carne_estudiante;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public String getCarne_estudiante() {
        return carne_estudiante;
    }

    public void setCarne_estudiante(String carne_estudiante) {
        this.carne_estudiante = carne_estudiante;
    }
    
    
    
}
